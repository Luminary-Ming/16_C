/*
	打印五子棋棋盘
*/
#include <stdio.h>

int main(void)
{
	printf("0");

	for (int i = 1; i <= 15; i++) {
		printf(" %X", i);
	}

	printf("\n");

	for (int i = 1; i <= 15; i++) {

		printf("%X ", i);

		for (int j = 1; j <= 15; j++) {
			printf("+ ");
		}

		printf("\n");
	}

	return 0;
}
